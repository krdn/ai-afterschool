// 생성할 파일 경로
const OUTPUT_PATH = path.resolve(
  __dirname,
  "../src/lib/analysis/hanja-strokes-data.ts"
)

function generateCode(data: Record<string, number>): string {
  // 획수별로 그룹화하여 정렬
  const entries = Object.entries(data).sort((a, b) => {
    if (a[1] !== b[1]) return a[1] - b[1]
    return a[0].localeCompare(b[0])
  })

  const lines: string[] = []
  lines.push("/**")
  lines.push(" * 한국 인명용 한자 획수 데이터 (자동 생성)")
  lines.push(` * 생성일: ${new Date().toISOString().slice(0, 10)}`)
  lines.push(` * 총 ${entries.length}자`)
  lines.push(" * 출처: Unicode Consortium Unihan Database (kTotalStrokes)")
  lines.push(" *")
  lines.push(" * 이 파일은 scripts/generate-hanja-strokes.ts로 생성되었습니다.")
  lines.push(" * 수동 편집하지 마세요.")
  lines.push(" */")
  lines.push("")
  lines.push("export const HANJA_STROKES_DATA: Record<string, number> = {")

  // 획수별로 그룹화하여 주석 표시
  let currentStroke = -1
  for (const [char, stroke] of entries) {
    if (stroke !== currentStroke) {
      if (currentStroke !== -1) lines.push("")
      lines.push(`  // ${stroke}획`)
      currentStroke = stroke
    }
    lines.push(`  ${char}: ${stroke},`)
  }

  lines.push("}")
  lines.push("")

  return lines.join("\n")
}

function main() {
  // 중복 제거 (같은 한자가 여러 번 등록된 경우)
  const uniqueData: Record<string, number> = {}
  for (const [char, stroke] of Object.entries(HANJA_STROKE_DATA)) {
    if (uniqueData[char] && uniqueData[char] !== stroke) {
      console.warn(`경고: '${char}'의 획수가 중복 등록됨 (${uniqueData[char]} vs ${stroke})`)
    }
    uniqueData[char] = stroke
  }

  const code = generateCode(uniqueData)
  fs.writeFileSync(OUTPUT_PATH, code, "utf-8")

  const count = Object.keys(uniqueData).length
  console.log(`✅ ${count}개 한자 획수 데이터 생성 완료`)
  console.log(`   출력: ${OUTPUT_PATH}`)
}

main()
